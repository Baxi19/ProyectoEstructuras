    private void buttonDeleteUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonDeleteUserActionPerformed
        // TODO add your handling code here:
        if(TextFieldUserDelete.getText().isEmpty())
            JOptionPane.showMessageDialog(rootPane, "Empty field");
        else{
            String name = TextFieldUserDelete.getText();
            ListPlayersRegistered.getInstance().deletePlayer(name);
            JOptionPane.showMessageDialog(rootPane, "Player " + name + " succesfully deleted.");
            TextFieldUserDelete.setText("");      
       }
    }//GEN-LAST:event_buttonDeleteUserActionPerformed

    private void buttonSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSearchActionPerformed
        // TODO add your handling code here:
        if(TextFieldUserDelete.getText().isEmpty())
            JOptionPane.showMessageDialog(rootPane, "Type a player to eliminate");
        else{
            Player userX = ListPlayersRegistered.getInstance().searchPlayer(TextFieldUserDelete.getText());
            if(userX == null)
                labelResult.setText("No matches found");
            else{
                labelResult.setText(userX.getPassword());
            }
        }
        
    }//GEN-LAST:event_buttonSearchActionPerformed

